                <li>Murder and Assault</li>
                <li>Medical Expense Reimbursement</li>
                <li>Flying Coverage</li>
                <li>Burial Expense</li>
                <li>An additional cover for Life insurance may be added under Personal accident</li>
            </ul>
            <p>Personal accident insurance may cover an individual, family, group, or a corporation. However, insurance providers have certain minimum qualifications for all personal accident insurance applicants. The most common requirements are the following:</p>
