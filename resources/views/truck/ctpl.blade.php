        <h2>Coverages of CTPL</h2>
        <p>Unlike comprehensive insurance policies in the Philippines, CTPL has a limited coverage since it is chiefly focused on the welfare of the third-party in case of an accident.</p>
        <p>Here are some of the benefits provided by a compulsory third-party liability insurance.</p>
        <ul class="check-list">
            <li>Death Indemnity</li>
            <p>CTPL providers offer a cash benefit of a minimum of P70,000 to the third-party person in case of death.</p>
            <li>Body Injuries and Fractures</li>
            <p>CTPL insurers provide hospital cash benefit to the third-party person in the event of hospitalization. These benefits include hospital and operating rooms, surgical expenses, drugs and medicine, and ambulance service, among others.</p>
            <li>Permanent Disablement</li>
            <p>A cash benefit is provided to a third-party person in the event of dismemberment. Each body part corresponds a certain amount of cash benefit. For example, if the person loses sight of both eyes, he/she can get a cash benefit amounting to P50,000 while if the loss of one eye, the cash benefit will be P20,000.</p>
        </ul>
